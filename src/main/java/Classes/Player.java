public class Player {
    private String name;
    private int games;
    private int gameWins;
    private int rounds;
    private int roundWins;
    private int score;

    
    public String getName() {
        return name;
    }

    public int getGames() {
        return games;
    }

    public int getGameWins() {
        return gameWins;
    }

    public int getRounds() {
        return rounds;
    }

    public int getRoundWins() {
        return roundWins;
    }

    public int getScore() {
        return score;
    }
    
    
}
