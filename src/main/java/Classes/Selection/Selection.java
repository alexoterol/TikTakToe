package Classes.Selection;

/**
 *
 * @author alexo
 */
public abstract class Selection {
    protected int x;
    protected int y;

    public Selection(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
    
    
}
